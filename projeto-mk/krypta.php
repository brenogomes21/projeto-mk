								<h2> A Krypta está de volta !!!</h2>
								<p> O modo que foi lançado em Mortal Kombat X, chega à nova versão reformulado. Agora todo em terceira
								pessoa, você precisa cruzar a Ilha de Shang Tsung, icônico cenário do jogo, atrás de baús e recompensas para
								liberar novos itens de personalização, modificadores, equipamentos, e até Fatalities secretos. A Kripta ainda mantém 
								o sistema de utilização de moedas virtuais para que esses baús sejam conquistado. O problema é a dificuldade 
								para conseguir estas moedas, já que a única forma de consegui-las é jogando (e muito) 
								os outros modos de batalha. A própria história, que leva mais de 5 horas para ser concluída, só fornece
								cerca de 100 mil moedas, o que só permite comprar em torno de 10 a 20 baús.
