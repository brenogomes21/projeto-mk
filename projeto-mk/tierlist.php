						<h2> Roster no competitivo </h2>
						<p> O Mortal Kombat 11 já está fora há alguns meses, com milhares de jogadores ao redor do mundo, colocando seus punhos nos 
						rostos um do outro em prol de uma violência extraordinária. Desde então, muito tempo foi gasto mergulhando fundo no elenco de 
						personagens do jogo, a fim de descobrir o quão bom eles realmente são. Entre isso e o recém lançado Shang Tsung , a meta mudou 
						significativamente desde o lançamento. Cada personagem do Mortal Kombat 11 se encaixa em um dos três arquétipos gerais
						do jogo de luta; aqueles sendo Rushdown (que se refere a personagens que estão buscando colocar pressão no inimigo), Grapplers 
						(que misturam ofensiva padrão com agarrão) e Zoners (que se destacam em manter oponentes à distância com uma variedade de movimentos
						especiais). Classificamos todos os lutadores do Mortal Kombat 11 em uma lista de níveis únicos. Nós olhamos 
						para uma série de fatores como os movimentos especiais de um personagem, o alcance e a velocidade de seus movimentos 
						normais, a facilidade com que seus combos de alto dano eram executados, e assim por diante. Há duas coisas a ter em mente
						- uma, que esta lista é subjetiva, e é baseada em nossa própria opinião de quão bom é um personagem. Dois, vale a pena notar
						que todos os personagens são viáveis. Se você gosta de jogar de Kung Lao, então jogue de Kung Lao! Só porque um personagem 
						é mediano aos nossos olhos não os torna impossíveis de jogar. Quando classificamos os personagens, usamos duas variações 
						de torneio, pois elas eram especificamente balanceadas para o jogo competitivo e atuam como um ponto de comparação universal 
						para todos os personagens. Se você não é fã de uma variação específica de torneio, pode torná-lo seu - há um grande número 
						de movimentos especiais para você experimentar que podem fazer com que um personagem seja mais adequado ao seu estilo de 
						jogo. O Mortal Kombat 11 certamente mudará com o tempo. Com os personagens de DLC que serão lançados após o lançamento 
						junto com os patches contínuos, a colocação de lutadores nesta lista certamente será abalada. <br> Confira a Tier List dos personagens abaixo :
