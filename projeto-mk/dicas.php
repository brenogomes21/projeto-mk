								<h2> Jogue o modo tutorial </h2>
								<p> O modo tutorial pode parecer desnecessário para os jogadores mais experientes nos jogos de luta, mas 
								a Netherrealm demonstrou um carinho especial para as dicas em Mortal Kombat 11. Como todo treino, o game começa mostrando
								controles básicos como movimentação e botões de ação, mas os jogadores não perdem por esperar: após dicas para iniciantes, 
								o game abrirá a possibilidade de entender estratégias de pro players, ensinando e aplicando conhecimentos
								sobre dados de frames, vantagem e desvantagem de defesa e afins. Também vale ressaltar que as informações apresentadas 
								no tutorial de Mortal Kombat 11 são úteis quando aplicadas a qualquer jogo de luta, não apenas ao game da Netherrealm.
								</p>
								<h3> Aprenda a configurar o treino </h3> 
								<p>   Após absorver tudo que os tutoriais têm a oferecer, está na hora de praticar o que aprendeu. 
								No modo treino é possível simular as mais específicas situações para saber como lidar com o jogo dos inimigos. Ao lutar em 
								outras experiências oferecidas por MK11 (torres, histórias, entre outros), com certeza o jogador vai bater 
								de frente com certos ataques que parecerão imbatíveis à primeira vista, mas ao explorá-los a fundo, a solução
								pode dar as caras. O modo treino de Mortal Kombat 11 oferece diferentes layouts que apresentarão informações 
								distintas e relevantes sobre tudo que acontece durante as partidas. Além disso, há também opções de configuração
								com definições de inteligência artificial, gravações e afins - explore estes recursos ao máximo.
								</p>
								<h4> Saiba administrar as novas barras </h4>
								<p> Diferente de qualquer outro game da franquia, Mortal Kombat 11 apresenta dois tipos diferentes de barras para
								serem usadas em situações distintas: uma ofensiva e outra defensiva. A barra ofensiva é a que mais se assemelha 
								aos conhecidos recursos de jogos de luta, utilizada para aprimorar ataques especiais ou mudar certas propriedades. A barra
								defensiva faz parte de um sistema mais complexo, podendo ser gasta para recuperação 
								(interrompendo combos do adversário) e get-ups (ataques disponíveis ao levantar). Além disso, certos ataques 
								especiais têm propriedades particulares utilizando esta barra, já que é possível incorporar armaduras em certos movimentos. A principal dica
								é administrar estes recursos valiosos com cuidado, já que podem determinar o andamento de uma partida.
								</p>
